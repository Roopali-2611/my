public class loops {
    public static void main(String[] args) {
        // syntax of for loop:
        /*
        for(initialisation;condition;incrementation){
        body}
        */
        for(int num=1; num<5; num++){
            System.out.println("hello baby");
        }
        int num = 1;
        while(num < 5){
            System.out.println(num);
            num++;
        }
        /*do while
         * do{
         * whilee(conditon)}
         */
        int n=1 ;
        do{
            System.out.println("hello");
        }while(n!=1);
    }
    
}
 